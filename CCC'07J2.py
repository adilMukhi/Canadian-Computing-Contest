text = ""

while text != "TTYL":
    text = input()
    if text == "CCC":
        print("Canadian Computing Competition")
    elif text == "CUZ":
        print("because")
    elif text == "TY":
        print("Thank-you")
    elif text == "YW":
        print("You're welcome")
    elif text == "CU":
        print("see you")
    elif text == "TA":
        print("totally awesome")
    elif text == ":-)":
        print("I'm happy")
    elif text == ":(":
        print("I'm unhappy")
    elif text == ";-)":
        print("wink")
    elif text == ":-P":
        print("stick out my tongue")
    elif text == "(~.~)":
        print("sleepy")
    else:
        print(text)

print("talk to you later")